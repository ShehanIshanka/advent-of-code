def get_val11(d, v):
    x = v

    for i, j in d.items():
        if i[0] <= v and v <= i[1]:
            x = j[0] + v - i[0]
            break

    return x


def get_val(d, v):
    x = v
    for i, j in d.items():
        if i[0] == v:
            x = j[0]
            break
        if i[1] == v:
            x = j[1]
            break

        if i[0] < v < i[1]:
            x = j[0] + v - i[0]
            break

    return x


def get_val2(d, v):
    for i, j in d.items():
        if i[0] <= v and v < i[1]:
            return j[0] + v - i[0]


def read_map(splits, start):
    d = {}
    mi, ma = float("inf"), 0
    row = splits[start]
    while row.strip() != "":
        start = start + 1
        row = splits[start]
        if row == "":
            break
        destination, source, length = row.split()
        d[(int(source), int(source) + int(length) - 1)] = (
            int(destination),
            int(destination) + int(length) - 1,
        )
        mi = min(mi, int(source))
        ma = max(ma, int(source) + int(length) - 1)

    if mi != 0:
        d[(0, mi - 1)] = (get_val(d, 0), get_val(d, mi - 1))

    d[(ma + 1, float("inf"))] = (get_val(d, ma + 1), get_val(d, float("inf")))

    return dict(sorted(d.items())), start


def read_data(file_name: str) -> Dict[str, List[Set[int]]]:
    with open(file_name, "r") as file:
        data = file.read()

    splits = data.split("\n")
    i = 0
    inputs = []
    data_map = {}

    while i < len(splits):
        row = splits[i]

        if i == 0:
            inputs.extend(list(map(int, row.split(": ")[1].split())))
        elif row in maps:
            data_map[row], i = read_map(splits, i)

        i = i + 1

    return inputs, data_map


